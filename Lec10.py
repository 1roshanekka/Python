# Arithmetic Operators
# +  -  *  /  //  %  **

print('Addition', 2+3)
print('Subtraction', 9-1)
print('Multiplication', 5*4)
print('Division', 7/3)
print()

print("Floor Division", 7//3)
print("It gives integer value of division, or QUOTIENT")
print()

print("Modulus", 7%3)
print("It gives the Remainder")
print()

print("Exponential", 6**2)
print("left to the power of right")
print()

